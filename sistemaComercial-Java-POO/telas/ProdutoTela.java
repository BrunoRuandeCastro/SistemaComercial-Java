    public void montaCombo(){
        listaGrupos = HibernateUtil.getSession().createQuery("from GrupoProduto").list();
        cGrupo.removeAllItems();
        for(GrupoProduto gp : listaGrupos){
            cGrupo.addItem(gp.getNome());
        }        
    }

    public void validaTela(String acao) {
        if (acao.equals("inicio")) {
            cNome.setEnabled(false);
            cEstoque.setEnabled(false);
            cGrupo.setEnabled(false);
            cValor.setEnabled(false);
            btNovo.setEnabled(true);
            btEditar.setEnabled(false);
            btExcluir.setEnabled(false);
            btCancelar.setEnabled(false);
            btSalvar.setEnabled(false);
            btSair.setEnabled(true);
        }else if(acao.equals("novo")){
            cNome.setEnabled(true);
            cEstoque.setEnabled(true);
            cGrupo.setEnabled(true);
            cValor.setEnabled(true);
            btNovo.setEnabled(false);
            btEditar.setEnabled(false);
            btExcluir.setEnabled(false);
            btCancelar.setEnabled(true);
            btSalvar.setEnabled(true);
            btSair.setEnabled(false);
        }else if(acao.equals("selecionado")){
            cNome.setEnabled(false);
            cEstoque.setEnabled(false);
            cGrupo.setEnabled(false);
            cValor.setEnabled(false);
            btNovo.setEnabled(true);
            btEditar.setEnabled(true);
            btExcluir.setEnabled(true);
            btCancelar.setEnabled(false);
            btSalvar.setEnabled(false);
            btSair.setEnabled(true);
        }
    }

