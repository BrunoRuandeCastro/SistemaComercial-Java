    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        if (validaCampos()) {
            if (cCodigo.getText().equals("")) {
                produto.setId(null);
            } else {
                produto.setId(Long.parseLong(cCodigo.getText()));
            }

            produto.setNome(cNome.getText());
            produto.setEstoque(Double.parseDouble(cEstoque.getText()));            
            produto.setPreco(Double.parseDouble(cValor.getText().replace(".", "").replace(",", ".")));
            produto.setGrupoProduto(listaGrupos.get(cGrupo.getSelectedIndex()));
            
            HibernateUtil.beginTransaction();
            HibernateUtil.getSession().merge(produto);
            HibernateUtil.commitTransaction();
            HibernateUtil.closeSession();

            montaTabela();
            limpaCampos();
            validaTela("inicio");
        }
    }//GEN-LAST:event_btSalvarActionPerformed

    public void limpaCampos() {
        cCodigo.setText("");
        cNome.setText("");
        cEstoque.setText("");
        cValor.setText("");
        cGrupo.setSelectedItem("");
    }

    public boolean validaCampos() {
        if (cNome.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "O nome é obrigatório!");
            return false;
        }
        if (cEstoque.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "A Estoque é obrigatória!");
            return false;
        }
        if (cGrupo.getSelectedItem().equals("")) {
            JOptionPane.showMessageDialog(null, "O campo grupo de produto é obrigatório!");
            return false;
        }
        return true;
    }

