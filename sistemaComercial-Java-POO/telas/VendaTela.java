    public void montaTabela() {
        listaVendas = HibernateUtil.getSession().createQuery("from Venda").list();
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Código");
        modelo.addColumn("Data");
        modelo.addColumn("Cliente");
        modelo.addColumn("Total");
        for (Venda ve : listaVendas) {
            modelo.addRow(new Object[]{ve.getId(), ve.getDataVenda(), ve.getCliente().getNome(), ve.getValorTotal()});
        }
        tabela.setModel(modelo);
    }

    public void montaTabelaItens() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Produto");
        modelo.addColumn("Preço");
        modelo.addColumn("quantidade");
        modelo.addColumn("SubTotal");
        for (ItensVenda it : venda.getItensVendas()) {
            modelo.addRow(new Object[]{it.getProduto().getNome(), it.getValor(), it.getQuantidade(), it.getSubTotal()});
        }
        tabelaItens.setModel(modelo);
    }

    private void limpacamposItens() {
        cProduto.setSelectedItem(null);
        cPreco.setText("");
        cQuantidade.setText("");
    }
