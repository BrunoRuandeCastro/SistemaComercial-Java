    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        if (validaCampos()) {
            if (cCodigo.getText().equals("")) {
                venda.setId(null);
            } else {
                venda.setId(Long.parseLong(cCodigo.getText()));
            }
            venda.setDataVenda(cData.getDate());
            venda.setCliente(listaClientes.get(cCliente.getSelectedIndex()));

            HibernateUtil.beginTransaction();
            HibernateUtil.getSession().merge(venda);
            HibernateUtil.commitTransaction();
            HibernateUtil.closeSession();

            for (ItensVenda it : venda.getItensVendas()) {
                Produto p = it.getProduto();
                p.setEstoque(p.getEstoque() - it.getQuantidade());
                HibernateUtil.beginTransaction();
                HibernateUtil.getSession().merge(p);
                HibernateUtil.commitTransaction();
                HibernateUtil.closeSession();
            }

            montaTabela();
            limpaCampos();
            validaTela("inicio");
        }
    }//GEN-LAST:event_btSalvarActionPerformed

    public void limpaCampos() {
        cCodigo.setText("");
        cData.setDate(null);
        cProduto.setSelectedItem(null);
        cCliente.setSelectedItem(null);
        cPreco.setText("");
        cQuantidade.setText("");
        cTotal.setText("R$ 0,00");
        DefaultTableModel modelo = new DefaultTableModel();
        tabelaItens.setModel(modelo);
    }

    public boolean validaCampos() {
        if (cData.getDate() == null) {
            JOptionPane.showMessageDialog(null, "A data é obrigatória!");
            return false;
        }
        if (cCliente.getSelectedItem().equals("")) {
            JOptionPane.showMessageDialog(null, "O Cliente é obrigatória!");
            return false;
        }
        return true;
    }

